public enum Color {
	// ===========================================================
	// Elements
	// ===========================================================

	CLUB,  // Kreuz
	DIAMOND,
	HEART,
	SPADE; // PIK

	// ===========================================================
	// Constants
	// ===========================================================

	// ===========================================================
	// Fields
	// ===========================================================

	// ===========================================================
	// Constructors
	// ===========================================================

	// ===========================================================
	// Getter & Setter
	// ===========================================================

	// ===========================================================
	// Methods from SuperClass/Interfaces
	// ===========================================================

	// ===========================================================
	// Methods
	// ===========================================================

	public String toString()
	{
		switch (this)
		{
		case CLUB:
			return "Club";	// \u2663
		case DIAMOND:
			return "Diamond";	// \u2666
		case HEART:
			return "Heart";	// \u2665
		case SPADE:
			return "Spade";	// \u2660
		default:
			throw new InternalError();
		}
	}

	// ===========================================================
	// Inner and Anonymous Classes
	// ===========================================================
}
